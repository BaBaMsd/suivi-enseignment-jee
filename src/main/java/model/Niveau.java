package model;

public class Niveau {
    private int id;
    private String niveau;
    
    
	public Niveau() {
		super();
	}
	public Niveau(String niveau) {
		super();
		this.niveau = niveau;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getNiveau() {
		return niveau;
	}
	public void setNiveau(String niveau) {
		this.niveau = niveau;
	}

    // Constructeur, getters et setters
}